package com.syntax.class07;

public class WhileDoWhile {
	public static void main(String[] args) {
		
		
		
		//hello 5 times
		
		int num=1;
		
		while(num<=5) {
			System.out.println("Hello");
			num++;
			
		}
		System.out.println("----------Using do while loop------------");
		
		
		//hello 5 times
		
		int num1=1;
		
		do {
			System.out.println("Hello");
			num1++;
			
		}while (num1<=5);
	}

}
