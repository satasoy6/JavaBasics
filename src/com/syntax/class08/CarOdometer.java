package com.syntax.class08;

public class CarOdometer {

	public static void main(String[] args) {
		
		
		for (int x = 0; x<10; x++) {
			for(int y = 0; y<10 ; y++) {
				for (int z = 0 ; z<10 ; z++) {
					for(int t = 0; t<10 ; t++) {
						
						System.out.println(x+" "+y+" "+z+" "+t);
					}
				}
				
			}
		}

	}

}
